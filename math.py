import math

pi = math.pi
negative_number = -9
e = math.e

print(pi)
print(e)

# round of
print(round(pi, 7))

# ceiling
print(math.ceil(pi))

# floor
print(math.floor(pi))

# getting absolute number (no negatives)
print(abs(negative_number))

# power
print(pow(math.pi, math.pi))

# square root
print(math.sqrt(math.pi))

# max
print(max(pi, e))

# min
print(min(pi, e))
